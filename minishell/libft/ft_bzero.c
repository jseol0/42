#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = (unsigned char *)p;
	i = 0;
	while (i < n)
	{
		*tmp++ = 0;
		i++;
	}
}
