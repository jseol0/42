#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*end;

	end = ft_lstlast(*lst);
	if (!end)
		*lst = new;
	else
	{
		end->next = new;
		new->next = 0;
	}
}
