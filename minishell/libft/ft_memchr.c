#include "libft.h"

void	*ft_memchr(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*tmp == (unsigned char)c)
			return (tmp);
		tmp++;
		i++;
	}
	return (0);
}
